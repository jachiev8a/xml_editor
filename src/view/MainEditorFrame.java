    public void update(EventBaseClass evt){
        if ( evt instanceof EventUpdateTracePointList ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jListTracePoints.setModel(
                this.controller.getTracePointList()
            );
        }
        else if ( evt instanceof EventUpdateTraceCommandList ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jListTraceCommands.setModel(
                this.controller.getTraceCommandList()
            );
        }
        else if ( evt instanceof EventUpdatePriorityClassList ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jListPriorityClasses.setModel(
                this.controller.getPriorityClassList()
            );
        }
        else if ( evt instanceof EventUpdateAllLists ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.update( new EventUpdateTracePointList() );
            this.update( new EventUpdateTraceCommandList() );
            this.update( new EventUpdatePriorityClassList() );
        }
        else if ( evt instanceof EventUpdateCurrentComponentNameLabel ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jLabelComponentName.setText(this.currentComponentName);
        }
        else if ( evt instanceof EventUpdateCurrentTcmPath ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jTextFieldTcmPath.setText(this.currentFileLoaded);
            this.fileIsAlreadyLoaded = true;
        }
        else if ( evt instanceof EventUpdateTcmPathForegroundToRed ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jTextFieldTcmPath.setForeground(Color.RED);
        }
        else if ( evt instanceof EventUpdateTcmPathForegroundToNormal ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jTextFieldTcmPath.setForeground(Color.DARK_GRAY);
        }
        else if ( evt instanceof EventDisableAllComponentControls ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jButtonAddTracePoint.setEnabled(false);
            this.jButtonAddTraceCommand.setEnabled(false);
            this.jButtonAddPriorityClass.setEnabled(false);
            this.jButtonEditTracePoint.setEnabled(false);
            this.jButtonEditTraceCommand.setEnabled(false);
            this.jButtonEditPriorityClass.setEnabled(false);
            this.jButtonRemoveTracePoint.setEnabled(false);
            this.jButtonRemoveTraceCommand.setEnabled(false);
            this.jButtonRemovePriorityClass.setEnabled(false);
            this.jButtonSaveTcm.setEnabled(false);
            
            this.jListTracePoints.setEnabled(false);
            this.jListTraceCommands.setEnabled(false);
            this.jListPriorityClasses.setEnabled(false);
            
            this.jLabelTracepoints.setEnabled(false);
            this.jLabelTraceCommands.setEnabled(false);
            this.jLabelPriorityClasses.setEnabled(false);
            this.jLabelComponent.setEnabled(false);
        }
        else if ( evt instanceof EventEnableAllComponentControls ){
            Logger.getGlobal().info(
                String.format("[View] Event update: \"%s\"", evt.getClass().getSimpleName()));
            this.jButtonAddTracePoint.setEnabled(true);
            this.jButtonAddTraceCommand.setEnabled(true);
            this.jButtonAddPriorityClass.setEnabled(true);
            this.jButtonEditTracePoint.setEnabled(true);
            this.jButtonEditTraceCommand.setEnabled(true);
            this.jButtonEditPriorityClass.setEnabled(true);
            this.jButtonRemoveTracePoint.setEnabled(true);
            this.jButtonRemoveTraceCommand.setEnabled(true);
            this.jButtonRemovePriorityClass.setEnabled(true);
            this.jButtonSaveTcm.setEnabled(true);
            
            this.jListTracePoints.setEnabled(true);
            this.jListTraceCommands.setEnabled(true);
            this.jListPriorityClasses.setEnabled(true);
            
            this.jLabelTracepoints.setEnabled(true);
            this.jLabelTraceCommands.setEnabled(true);
            this.jLabelPriorityClasses.setEnabled(true);
            this.jLabelComponent.setEnabled(true);
        }
    }

