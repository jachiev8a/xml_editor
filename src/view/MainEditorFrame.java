    private void jButtonOpenTcmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOpenTcmActionPerformed
        boolean userOption = true;
        if(this.fileIsAlreadyLoaded){
            userOption = this.componentHandler.showAcceptCancelDialog(
                this,
                "TCM file '...' is already loaded, are you sure you want to continue?");
        }
        if(userOption == true){
            String fileName = this.componentHandler.chooseTcmFileFromWindowsExplorer(this);
            if(!(fileName == null)){
                this.componentHandler.setTextInTextFieldComponent(this.jTextFieldTcmPath, fileName);
                this.currentFileLoaded = fileName;
                this.fileIsAlreadyLoaded = true;
                //Parse file
                this.controller.openTcmFile(fileName);
                this.update( new EventEnableAllComponentControls() );
            }
        }
    }//GEN-LAST:event_jButtonOpenTcmActionPerformed

